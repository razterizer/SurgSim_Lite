template<int NR, int NC>
void draw_title(SpriteHandler<NR, NC>& sh, const ASCII_Fonts::FontDataColl& font_data, const std::vector<ASCII_Fonts::ColorScheme>& font_colors, const std::string& exe_folder)
{
  //const auto wht = Text::Color::White;
  //const auto lgry = Text::Color::LightGray;
  const auto dgry = Text::Color::DarkGray;
  //const auto blk = Text::Color::Black;
  const auto red = Text::Color::Red;
  //const auto dred = Text::Color::DarkRed;
  //const auto cyn = Text::Color::Cyan;
  const auto trp = Text::Color::Transparent;
  
  // Simplifies typing.
  auto print = [&sh](const std::string& str, int r, int c, Text::Color fg_color, Text::Color bg_color = Text::Color::Transparent)
  {
    sh.write_buffer(str, r, c, fg_color, bg_color);
  };
  
  print("Rasmus Anthin", 0, 55, red, trp);
  print("Presents:", 1, 57, red, trp);
  
  std::vector<std::string> lines;
  if (TextIO::read_file(folder::join_file_path({ exe_folder, "title.txt" }), lines) && !lines.empty())
    ASCII_Fonts::draw_text(sh, font_data, font_colors[0], lines.back(), 1, 8, ASCII_Fonts::Font::Larry3D);
  else
    ASCII_Fonts::draw_text(sh, font_data, font_colors[0], "SurgSim", 1, 8, ASCII_Fonts::Font::Larry3D);
  ASCII_Fonts::draw_text(sh, font_data, font_colors[1], "Lite", 11, 31, ASCII_Fonts::Font::Larry3D);
  
  // Cholecystectomy
  ASCII_Fonts::draw_text(sh, font_data, font_colors[2], "Cholecystectomy", 19, 3, ASCII_Fonts::Font::SMSlant);
  ASCII_Fonts::draw_text(sh, font_data, font_colors[2], "Edition", 24, 3, ASCII_Fonts::Font::SMSlant);
  

  print("Press space-bar to continue...", 29, 24, dgry);
  print("(c) 2022", 29, 65, dgry);
}

